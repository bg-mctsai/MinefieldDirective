function bitmapToPlayable(shapes, id) {
  const shape = shapes[id];
  if (!shape) return null;
  const { W, H, rows } = shape;
  const p = new Set();
  for (let y = 0; y < H; y++) {
    for (let x = 0; x < W; x++) {
      if (rows[y][x] === '#') p.add(key(x, y));
    }
  }
  return { W, H, playable: p };
}

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const MAPS_DIR = path.resolve(__dirname, '../renderer/src/levelData/maps');

const THEMES5 = Object.values(CH5_SHAPES).map((s) => s.theme);

const THEMES6 = Object.values(CH6_SHAPES).map((s) => s.theme);

const THEMES7 = Object.values(CH7_SHAPES).map((s) => s.theme);

function layoutIndexAndSil(id) {
  if (id <= 50) {
    const li = id - 41;
    if (id <= 42) return { li, full: true, sil: 0 };
    return { li, full: false, sil: id - 43 };
  }
  if (id <= 60) {
    const li = (id - 51 + 3) % 10;
    if (id <= 52) return { li, full: true, sil: 0 };
    return { li, full: false, sil: id - 53 };
  }
  const li = (id - 61 + 6) % 10;
  if (id <= 62) return { li, full: true, sil: 0 };
  return { li, full: false, sil: id - 63 };
}

function themeFor(id) {
  if (id <= 50) return THEMES5[id - 41];
  if (id <= 60) return THEMES6[id - 51];
  return THEMES7[id - 61];
}

const ALL_SHAPES = { ...CH5_SHAPES, ...CH6_SHAPES, ...CH7_SHAPES };

const LEVELS_PATH = path.resolve(__dirname, '../renderer/src/levelData/levels.json');
const levelsDoc = JSON.parse(fs.readFileSync(LEVELS_PATH, 'utf8').replace(/^\uFEFF/, ''));

for (let id = 41; id <= 70; id++) {
  const shape = ALL_SHAPES[id];
  let W;
  let H;
  let playable;
  let grid;
  if (shape) {
    ({ W, H, playable } = bitmapToPlayable(ALL_SHAPES, id));
    grid = shape.grid ?? 'HEXAGON';
  } else {
    const { li, full, sil } = layoutIndexAndSil(id);
    ({ width: W, height: H } = CH4_TRIANGLE_LAYOUTS[li]);
    playable = full ? fullBoard(W, H) : silhouettePlayable(sil, W, H);
    grid = 'HEXAGON';
  }
  const forbidden = forbiddenFromPlayable(W, H, playable);
  const n = playable.size;
  const cov = 0.8;
  const k = 1.08;
  const time = Math.max(id === 41 ? 72 : 0, Math.round(n * cov * k), 55);

  const mapLayout = grid === 'SQUARE'
    ? { type: 'SQUARE', width: W, height: H, forbiddenCells: forbidden }
    : { type: grid, placeholder: { width: W, height: H }, forbiddenCells: forbidden };

  const outPath = path.join(MAPS_DIR, `${id}.json`);
  fs.writeFileSync(outPath, `${JSON.stringify({ mapLayout, mapTheme: themeFor(id) }, null, 2)}\n`, 'utf8');

  const lv = levelsDoc.levels.find((l) => l.levelId === id);
  if (lv && lv.gridSystem !== grid) lv.gridSystem = grid;

  console.log(`L${id} ${grid} ${W}×${H} playable=${n} forbidden=${forbidden.length} time~${time} ${themeFor(id)}`);
}

fs.writeFileSync(LEVELS_PATH, `${JSON.stringify(levelsDoc, null, 2)}\n`, 'utf8');
console.log('\ngridSystem synced → levels.json');
